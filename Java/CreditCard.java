package Java;

import java.util.Date;

class CreditCard extends Payment{
    String entity;
    Date expDate;
    Integer cvv;

    public CreditCard(Payment id, String entity, Date expDate, Integer cvv){
        super(id);
        this.entity = entity;
        this.expDate = expDate;
        this.cvv = cvv;

        this.printDataPayment();
    }
}